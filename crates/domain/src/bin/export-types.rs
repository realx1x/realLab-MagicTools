fn main() -> Result<(), Box<dyn std::error::Error>> {
    let workspace = PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("../..");
    let package_source = workspace.join("packages/generated-types/src");
    let bindings = package_source.join("bindings");
    fs::create_dir_all(&bindings)?;

    let config = Config::new()
        .with_out_dir(&bindings)
        .with_large_int("number");
    DomainBindings::export_all(&config)?;

    let mut index =
        String::from("// Generated by `cargo run -p domain --bin export-types`. Do not edit.\n\n");
    index.push_str("export const GENERATED_TYPES_VERSION = 19 as const;\n");
    for name in EXPORTED_TYPES {
        index.push_str(&format!(
            "export type {{ {name} }} from './bindings/{name}';\n"
        ));
    }
    fs::write(package_source.join("index.ts"), index)?;
    Ok(())
}
